package com.spring.board;

public class SubmitLogDto {
	String Sub_num;
	String M_ID;
	String Prob_num;
	String Sub_answer;
	String Sub_code;
	
	public String getSub_num() {
		return Sub_num;
	}
	public void setSub_num(String sub_num) {
		Sub_num = sub_num;
	}
	public String getM_ID() {
		return M_ID;
	}
	public void setM_ID(String m_ID) {
		M_ID = m_ID;
	}
	public String getProb_num() {
		return Prob_num;
	}
	public void setProb_num(String prob_num) {
		Prob_num = prob_num;
	}
	public String getSub_answer() {
		return Sub_answer;
	}
	public void setSub_answer(String sub_answer) {
		Sub_answer = sub_answer;
	}
	public String getSub_code() {
		return Sub_code;
	}
	public void setSub_code(String sub_code) {
		Sub_code = sub_code;
	}

}
